char	*ft_strncat(char *s1, char *s2, int nb)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (s1[i])
		i += 1;
	while ((j < nb) && s2[j])
	{
		s1[i + j] = s2[j];
		j += 1;
	}
	s1[i + j] = '\0';
	return (s1);
}
