int		is_alpha(char c)
{
	return (c >= 65 && c <= 90) || (c >= 97 && c <= 122);
}

char	*ft_strlowcase(char *str)
{
	char *p;

	p = str;
	while (*str)
	{
		if (is_alpha(*str))
			if (*str < 97)
				*str += 32;
		str++;
	}
	return (p);
}
