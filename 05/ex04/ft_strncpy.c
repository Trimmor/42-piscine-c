char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	char *p;

	p = dest;
	while (n)
	{
		if (*src != '\0')
			*dest++ = *src++;
		else
			*dest++ = '\0';
		n--;
	}
	return (p);
}
