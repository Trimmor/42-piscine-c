char	*ft_strncat(char *dest, char *src, int nb)
{
	char *p;

	p = dest;
	while (*dest)
		dest++;
	while (nb && *src != '\0')
	{
		*dest++ = *src++;
		nb--;
	}
	*dest = '\0';
	return (p);
}
